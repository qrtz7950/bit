public class LoopMain02 {

	public static void main(String[] args) {

		System.out.print('*');
		System.out.print('*');
		System.out.print('*');
		System.out.print('*');
		System.out.print('*');
		
		System.out.println();
		
		for(int i=0; i<5; i++) {
			System.out.print('*');
		}

		System.out.println();
		
		for(int i=5;i>0;i--) {
			System.out.print(i);
		}
		
		System.out.println();
		
	}

}
